SELECT DEPT.DEPTNO, DEPT.DNAME, EMP.EMPNO, EMP.ENAME, EMP.MGR, EMP.SAL, EMP.DEPTNO, SALGRADE.LOSAL, SALGRADE.HISAL, SALGRADE.GRADE, EMP2.EMPNO AS MGR_EMPNO, EMP2.ENAME AS MGR_ENAME
FROM DEPT_YOOLIM DEPT, EMP_YOOLIM EMP, SALGRADE_YOOLIM SALGRADE, EMP_YOOLIM EMP2
WHERE DEPT.DEPTNO = EMP.DEPTNO(+)
AND EMP.SAL BETWEEN SALGRADE.LOSAL(+) AND SALGRADE.HISAL(+)
AND EMP2.EMPNO(+) = EMP.MGR
ORDER BY DEPT.DEPTNO, EMP.EMPNO;