SELECT DEPT.DEPTNO, DEPT.DNAME, EMP.EMPNO, EMP.ENAME, EMP.SAL
FROM DEPT_YOOLIM DEPT, EMP_YOOLIM EMP
WHERE EMP.DEPTNO = DEPT.DEPTNO
AND SAL > 2000
ORDER BY DEPT.DEPTNO, EMP.EMPNO;

SELECT DEPT.DEPTNO, DEPT.DNAME, AVG(EMP.SAL) AS AVG_SAL, MAX(EMP.SAL) AS MAX_SAL, MIN(EMP.SAL) AS MIN_SAL, COUNT(EMP.EMPNO) AS CNT
FROM DEPT_YOOLIM DEPT, EMP_YOOLIM EMP
WHERE DEPT.DEPTNO = EMP.DEPTNO
GROUP BY DEPT.DEPTNO, DEPT.DNAME
--HAVING DEPT.DEPTNO = EMP.DEPTNO --> 왜 HAVING은 안되징...
;

